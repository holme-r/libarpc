static int
string_s_cmp(string_s *a, string_s *b, int size)
{
	return strcmp(a->str, b->str);
}

static int
test_u_cmp(test_u *a, test_u *b, int size)
{
#define len test_u_u.string_s_list.string_s_list_len
#define val test_u_u.string_s_list.string_s_list_val
	int i;

	if (a->which != b->which)
		return 1;
	if (a->len != b->len)
		return 1;
	for (i = 0; i < a->len; i++) {
		if (string_s_cmp(&a->val[i], &b->val[i], -1) != 0)
			return 1;
	}
	return 0;
#undef len
#undef val
}

