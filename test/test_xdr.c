static void
_datadump(char *buf, size_t len)
{
	int i;
	for (i = 0; i < len; i++) {
		if (i != 0 && (i % 16) == 0) {
			printf("\n");
		}
		printf("%02hhx ", buf[i]);
	}
	printf("\n");
}

typedef int (*compare_t)(const void*, const void*, size_t);


static void
axdr_test_synchronous(axdrproc_t testfunc,
		      void *testobj, size_t size, compare_t cmp)
{
	size_t serialized_size;
	void *buf1, *buf2;
	axdr_ret_t rval;
	axdr_state_t src, dst;

	serialized_size = axdr_sizeof(testfunc, testobj);
	buf1 = malloc(serialized_size);
	ATF_REQUIRE(buf1 != NULL);

	/* encode */
	axdrmem_create(&src, (char*) buf1, serialized_size, AXDR_ENCODE);
	ATF_REQUIRE_MSG((rval = testfunc(&src, testobj)) == AXDR_DONE,
			"Unexpected encode return %d", rval);

	/* decode */
	axdrmem_create(&dst, (char *) buf1, serialized_size, AXDR_DECODE);
	buf2 = calloc(1, size);
	ATF_REQUIRE(buf2 != NULL);
	ATF_REQUIRE_MSG((rval = testfunc(&dst, buf2)) == AXDR_DONE,
			"Unexpected decode return %d", rval);
	ATF_REQUIRE(cmp(testobj, buf2, size) == 0);
	axdr_free(testfunc, buf2);
	free(buf2);

	_datadump(buf1, serialized_size);
	free(buf1);
}

static void
axdrrec_test_asyncrec(axdrproc_t testfunc, void *testobj,
		      size_t size, compare_t cmp, int size1, int size2,
		      bool_t log)
{
	size_t serialized_size;
	void *result;
	astk_t stack;
	asyncobj_t obj;
	axdr_ret_t ret;
	axdr_state_t xdr;
	int err;

	serialized_size = axdr_sizeof(testfunc, testobj);

	ATF_REQUIRE_MSG((err = axdrrec_create(&xdr, size1, size2, &obj,
					      &atest_read, &atest_write)) == 0,
			"unable to create src xrec err=%d", err);

	ATF_REQUIRE_MSG((err = astk_init(&stack)) == 0,
			"unable to create async stack err=%d", err);

	xdr.x_async = &stack;

	obj.state = 1;
	obj.off = 0;
	obj.buflen = serialized_size;
	obj.buf = malloc(serialized_size);
	ATF_REQUIRE(obj.buf != NULL);

	xdr.x_op = AXDR_ENCODE_ASYNC;

	/* encode it first */
	for (;;) {
		ret = (*testfunc)(&xdr, testobj);
		if (ret == AXDR_DONE) {
			break;
		}

		obj.state++;
		ATF_REQUIRE_MSG(ret == AXDR_WAITING,
				"encode error return=%d", ret);
	}

	/* flush it */
	for (;;) {
		ret = axdrrec_endofrecord(&xdr, TRUE);
		if (ret == AXDR_DONE) {
			break;
		}

		obj.state++;
		ATF_REQUIRE_MSG(ret == AXDR_WAITING,
				"flush error return=%d", ret);
	}

	if (log) {
		printf("encoded (%d/%d bytes)\n", obj.off,
		       (int32_t) serialized_size);
		_datadump(obj.buf, obj.off);
	}

	result = malloc(size);
	ATF_REQUIRE(result != NULL);
	memset(result, 0, size);

	xdr.x_op = AXDR_DECODE_ASYNC;

	obj.state = 0;
	obj.buflen = obj.off;
	obj.off = 0;

	for (;;) {
		ret = axdrrec_skiprecord(&xdr);
		if (ret == AXDR_DONE) {
			break;
		}

		obj.state++;
		ATF_REQUIRE_MSG(ret == AXDR_WAITING,
				"skip error return=%d", ret);
	}
	for (;;) {
		ret = (*testfunc)(&xdr, result);
		if (ret == AXDR_DONE) {
			break;
		}

		obj.state++;
		ATF_REQUIRE_MSG(ret == AXDR_WAITING,
				"decode error return=%d", ret);
	}

	free(obj.buf);
	axdr_destroy(&xdr);
	astk_cleanup(&stack);

	ATF_REQUIRE_MSG(cmp(testobj, result, size) == 0,
			"objects don't match");
	axdr_free(testfunc, result);
	free(result);
}


static void
axdrrec_test_async(axdrproc_t testfunc, void *testobj,
		   size_t size, compare_t cmp)
{
	size_t serialized_size;
	void *result;
	astk_t stack;
	asyncobj_t obj;
	axdr_ret_t ret;
	axdr_state_t xdr;
	int err;

	serialized_size = axdr_sizeof(testfunc, testobj);

	memset(&xdr, 0, sizeof(xdr));
	xdr.x_op = AXDR_ENCODE_ASYNC;
	xdr.x_ops = &aops;
	xdr.x_private = &obj;

	ATF_REQUIRE_MSG((err = astk_init(&stack)) == 0,
			"unable to create async stack err=%d", err);
	xdr.x_async = &stack;

	obj.state = 0;
	obj.off = 0;
	obj.buflen = serialized_size;
	obj.buf = malloc(serialized_size);
	ATF_REQUIRE(obj.buf != NULL);

	xdr.x_op = AXDR_ENCODE_ASYNC;

	/* encode it first */
	for (;;) {
		ret = (*testfunc)(&xdr, testobj);
		if (ret == AXDR_DONE) {
			break;
		}

		obj.state++;
		ATF_REQUIRE_MSG(ret == AXDR_WAITING,
				"encode error return=%d", ret);
	}

	ATF_REQUIRE_MSG(obj.off == serialized_size,
			"encode length mismatch %d/%zu",
			obj.off, serialized_size);

	printf("encoded\n");
	_datadump(obj.buf, obj.off);

	result = malloc(size);
	ATF_REQUIRE(result != NULL);
	memset(result, 0, size);

	xdr.x_op = AXDR_DECODE_ASYNC;

	obj.state = 0;
	obj.buflen = obj.off;
	obj.off = 0;

	for (;;) {
		ret = (*testfunc)(&xdr, result);
		if (ret == AXDR_DONE) {
			break;
		}

		obj.state++;
		ATF_REQUIRE_MSG(ret == AXDR_WAITING,
				"decode error return=%d", ret);
	}

	free(obj.buf);
	astk_cleanup(&stack);

	ATF_REQUIRE_MSG(cmp(testobj, result, size) == 0,
			"Objects don't match.");
	axdr_free(testfunc, result);
	free(result);
}

static void
axdr_test_print(axdrproc_t testfunc, void *testobj)
{
	axdr_state_t xdr;

	/* testout stringify */
	ATF_REQUIRE(axdr_fprint_create(&xdr, stdout, "axdr_test:") == 0);
	ATF_REQUIRE((*testfunc)(&xdr, testobj) == AXDR_DONE);
	axdr_destroy(&xdr);
}

static void
axdr_test(const char *name, axdrproc_t testfunc,
	  void *testobj, size_t size, compare_t cmp)
{
	printf("%s: print:\n", name);
	axdr_test_print(testfunc, testobj);
	printf("%s: sync:\n", name);
	axdr_test_synchronous(testfunc, testobj, size, cmp);
	printf("\n%s: async:\n", name);
	axdrrec_test_async(testfunc, testobj, size, cmp);
	printf("\n%s: async record:\n", name);
	axdrrec_test_asyncrec(testfunc, testobj, size,
			      cmp, 2000, 2000, TRUE);
	printf("\n");
}

