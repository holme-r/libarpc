static axdr_ret_t atest_getlong(axdr_state_t *, int32_t *);
static axdr_ret_t atest_putlong(axdr_state_t *, const int32_t *);
static axdr_ret_t atest_getbytes(axdr_state_t *, char *, size_t);
static axdr_ret_t atest_putbytes(axdr_state_t *, const char *, size_t);
static void atest_destroy(axdr_state_t *);

typedef struct asyncobj_s {
	int         state;
	int         off;
	char        *buf;
	int         buflen;
} asyncobj_t;


static int
atest_read(void *arg, void *buf, size_t *lenp)
{
	asyncobj_t *obj;
	size_t len;

	obj = (asyncobj_t *)arg;

	if (obj->state < 2) {
		return EAGAIN;
	}

	len = *lenp;

	if (len <= 0) {
		return 0;
	}

	if (obj->off >= obj->buflen) {
		*lenp = 0;
		return 0;
	}

	obj->state = 0;
	memcpy(buf, &obj->buf[obj->off], 1);
	*lenp = 1;
	obj->off++;
	return 0;
}


static int
atest_write(void *arg, void *buf, size_t *lenp)
{
	asyncobj_t *obj;
	size_t len;
	char *tmp;
	int tlen;

	obj = (asyncobj_t *)arg;

	if (obj->state < 2) {
		return EAGAIN;
	}

	len = *lenp;

	if (len <= 0) {
		return 0;
	}

	if (obj->off >= obj->buflen) {
		tlen = obj->buflen + 1024;
		tmp = realloc(obj->buf, tlen);
		if (!tmp) {
			*lenp = 0;
			return EINVAL;
		}
		obj->buf = tmp;
		obj->buflen = tlen;
	}

	obj->state = 0;
	memcpy(&obj->buf[obj->off], buf, 1);
	*lenp = 1;
	obj->off++;
	return 0;
}

static axdr_ret_t
atest_getlong(axdr_state_t *xdr, int32_t *lp)
{
	axdr_ret_t rval;
	bool_t cleanup;
	char *buf;
	long val;
	size_t len;
	int err;
	int off;

	off = 0;
	rval = axdr_async_setup(xdr, &atest_getlong, &cleanup, &off,
				sizeof(*lp), (void**)&buf);
	if (rval != AXDR_DONE) {
		return rval;
	}

	len = sizeof(*lp) - off;
	err = atest_read(xdr->x_private, &buf[off], &len);
	if (err != 0) {
		if (err == EAGAIN) {
			rval = AXDR_WAITING;
		} else {
			rval = AXDR_ERROR;
		}
		axdr_async_teardown(xdr, &atest_getlong, off, cleanup, rval);
		return rval;
	}

	off += len;
	if (off >= sizeof(*lp)) {
		memcpy(&val, buf, sizeof(val));
		*lp = ntohl(val);
		rval = AXDR_DONE;
	} else {
		rval = AXDR_WAITING;
	}

	axdr_async_teardown(xdr, &atest_getlong, off, cleanup, rval);
	return rval;
}

static axdr_ret_t
atest_putlong(axdr_state_t *xdr, const int32_t *lp)
{
	axdr_ret_t rval;
	bool_t cleanup;
	long val;
	size_t len;
	int err;
	int off;

	off = 0;
	rval = axdr_async_setup(xdr, &atest_putlong, &cleanup, &off,
				0, (void **)NULL);
	if (rval != AXDR_DONE) {
		return rval;
	}

	val = htonl(*lp);

	len = sizeof(*lp) - off;
	err = atest_write(xdr->x_private, &((char *)&val)[off], &len);
	if (err != 0) {
		if (err == EAGAIN) {
			rval = AXDR_WAITING;
		} else {
			rval = AXDR_ERROR;
		}
		axdr_async_teardown(xdr, &atest_putlong, off, cleanup, rval);
		return rval;
	}

	off += len;
	if (off >= sizeof(*lp)) {
		rval = AXDR_DONE;
	} else {
		rval = AXDR_WAITING;
	}
	axdr_async_teardown(xdr, &atest_putlong, off, cleanup, rval);
	return rval;
}

